<h1>Welcome to TestCenter</h1>
<h2>Introduction</h2>
<p>TestCenter aims to be the way you perform <a href="https://en.wikipedia.org/wiki/Acceptance_testing">UAT</a> or <a href="http://en.wikipedia.org/wiki/Functional_testing">Functional</a> for all types of projects (whether it be for a software project or the test phase of some system's/project's implementation).</p>
<p>The idea is simple. You create you:</p>
<ol>
  <li>Create Tests.</li>
  <li>Each Test, has as a series of Test Steps, that have to be executed in series.</li>
  <li>You sequence tests, as Test Sets, in order to test a particular feature of the implementation.</li>
  <li>You, or some other user, Runs a Test Set, and registers the result of the execution (of the tests and test steps).</li>
  <li>The system maintains a list of the runs and provides statistics on pass/failure of Tests and Test Sets.</li>
</ol>
<p>But first, we have to complete the implementation of TestCenter.</p>
<h2>Current State</h2>
<p>TestCenter is still under development....</p>
<p>The site you are seeing is an early alpha version (not all features implemented) site that I have made available for 3 reasons:</p>
<ol>
  <li>Allow other people to see, and try out, an early version of the application.</li>
  <li>Allow people to start participating, early on, in the project development. Be it through testing the system, ideas for the design or features they would like to see (i.e. get in on the ground floor of the development, and allow you to orient, within reasonable limits, the projects direction).</li>
</ol>
<p><strong>Note:</strong> </p>
<ul>
  <li>This test site is being hosted on a very limited piece of hardware I had lying around, so don't expect too much in the way of performance.</li>
  <li>Please don't deliberately try to destroy the system. Even though I can reset it's state with relative ease, I would still prefer to take away as little time from development as possible (But if you find a serious flaw in the security configuration of the system please tell me).</li>
</ul>
<h2>How can you use the site?</h2>
<p>So can you use the site?</p>
<ol>
  <li>Go back to the home page (i.e. Click the Home in the Main Menu).</li>
  <li>Login in to the System (i.e. Click the Login Action), and use 'admin'/'admin' as user and password in the login form and click Login.</li>
  <li>Navigate the system, creating tests / steps / sets or whatever is available for you to play with.</li>
</ol>
